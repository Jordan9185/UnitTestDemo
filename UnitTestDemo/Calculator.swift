import Foundation

class Calculator {
    
    func add(_ num1: Int, _ num2: Int) -> Int {
        
        return num1 + num2
        
    }
    
    func multiple(_ num1: Int, _ num2: Int) -> Int {
        
        return num1 * num2
        
    }
    
}
